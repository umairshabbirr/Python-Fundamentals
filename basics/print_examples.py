# Program: Print Examples in Python
# Purpose: To demonstrate different ways of using print() function

print("Hello, World!")

# Printing multiple values
print("Name:", "Umair", "Age:", 20)

# Printing numbers
print("Sum of 5 and 3 is:", 5 + 3)

# Using escape characters
print("Python\nProgramming")   # new line
print("Python\tProgramming")   # tab space

# Printing with separator
print("AI", "ML", "DL", sep=" | ")

# Printing without new line
print("Welcome to", end=" ")
print("Python")
