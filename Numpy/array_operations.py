# Program: Array Operations
# Purpose: To perform basic operations on NumPy arrays

import numpy as np

a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

print("Array A:", a)
print("Array B:", b)

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", b - a)

# Multiplication
print("Multiplication:", a * b)

# Division
print("Division:", b / a)
